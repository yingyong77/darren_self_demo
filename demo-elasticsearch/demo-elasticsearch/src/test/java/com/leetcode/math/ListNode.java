package com.leetcode.math;

/**
 * 链表
 *
 * @author : darren
 * @date : 2022/3/2
 */
public class ListNode {

    int val;
    ListNode next;

    ListNode() {
    }

    ListNode(int val) {
        this.val = val;
    }

    ListNode(int val, ListNode next) {
        this.val = val;
        this.next = next;
    }
}
